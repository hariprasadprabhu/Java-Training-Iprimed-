package employee.com;

public class Address {
	int doorNo;
	String village;
	String city;
	int pinCode;
	String state ;
	public Address(int doorNo, String village, String city, int pinCode, String state) {
		super();
		this.doorNo = doorNo;
		this.village = village;
		this.city = city;
		this.pinCode = pinCode;
		this.state = state;
	}
	
	

}
